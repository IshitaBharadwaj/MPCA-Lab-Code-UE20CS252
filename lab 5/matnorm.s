MOV R4,#0
B LOOPI

LOOPI:
ADD R3, R3, #1
MOV R4, #0
CMP R3, #4
BNE LOOPJ
CMP R3, #4
BEQ MAX1
LOOPJ:
ADD R4, R4,#1
CMP R4, #4
BNE COLSUM

BEQ LOOPI
COLSUM:
MOV R11, #12
MOV R12, #4
SUBS R6, R3, #1
MUL R6, R12, R6
LDR R7, [R1, R6]
SUB R8, R4,#1
MUL R11, R8, R11
SUBS R6, R3, #1
MUL R12, R6, R12
ADD R12, R12, R11
LDR R9,[R0, R12]
ADD R10, R7,R9
MOV R12, #4
SUBS R6, R3, #1
MUL R6, R12, R6
STR R10, [R1,R6]
B LOOPJ 

MAX1:
LDR R3,[R1]
LDR R4,[R1, #4]
LDR R5,[R1, #8]
CMP R3, R4
MOVGT R6, R3
CMP R3, R4
MOVLT R6, R4
B MAX2

MAX2:
CMP R6, R5
STRGT R6,[R2]
CMP R6, R5
STRLT R5, [R2]
SWI 0X011


